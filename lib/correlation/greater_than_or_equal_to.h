#include <stdio.h>

/**
 * Is greater than or equal to.
 * @params {double} a - Value a.
 * @params {double} b - Value b.
 * @returns Returns 1 if the value of a is greater than or equal to the value of b, otherwise returns 0.
 */
int greater_than_or_equal_to(double a, double b) {
	return a >=  b ? 1 : 0;
}

